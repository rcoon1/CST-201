package SinglyLinkedNode;

public class SinglyLinkedNode {

	public int data;

	public SinglyLinkedNode next;

	// default constructor

	public SinglyLinkedNode() {

		data = -1;

		next = null;

	}

	// constructor to initialise data of the node.

	public SinglyLinkedNode(int value) {

		data = value;

		next = null;

	}

}